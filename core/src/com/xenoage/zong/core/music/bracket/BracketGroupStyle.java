package com.xenoage.zong.core.music.bracket;


/**
 * Style of the bracket of a group of staves.
 * 
 * Possible styles are brace and bracket.
 * 
 * @author Andreas Wenger
 */
public enum BracketGroupStyle
{
  
  /** Curly brackets. */
  Brace,
  
  /** Square brackets. */
  Bracket,
  
  /** Line (TODO: not supported (rendered) yet) */
  Line;

}
